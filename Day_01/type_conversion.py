# In this program we will see some examples of type casting

# Python stores input values as an str

user_input = "95"
print(type(user_input))

# Now convert string into int so we can do some maths

marks = int(user_input)
print(type(marks))

print (marks+5)

# Convert int into float

price = 49
print (float(price))

# Convert no to str

age = 20
msg = "I am " + str(age) + " years old."

print(msg)
